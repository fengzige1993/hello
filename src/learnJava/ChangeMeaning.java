package learnJava;

public class ChangeMeaning {
    public static void main(String[] args){
        char charSingleQuote = '\'';
        //使用转移字符给字符变量赋值单引号
        char charNewline = '\n';
        //使用转义字符给字符变量赋值换行符
        char charDoubleQuotation = '"';
        //双引号不用转义字符
        System.out.println(charSingleQuote);
        System.out.println(charNewline);
        System.out.println(charDoubleQuotation);
    }
}
